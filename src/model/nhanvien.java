public class nhanvien {
    private String id, username, pass, chucvu;

    public nhanvien() {
    }

    public nhanvien(String id, String username, String pass, String chucvu) {
        this.id = id;
        this.username = username;
        this.pass = pass;
        this.chucvu = chucvu;
    }

    

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass= pass;
    }

    public String getChucvu() {
        return chucvu;
    }

    public void setChucvu(String chucvu) {
        this.chucvu = chucvu;
    }
    
}
